def _validate_news_format(uid, label, separator, signature):
    """
    يُجرّب إرسال خبر تجريبي بالتنسيق الجديد بنفس الشكل الحقيقي.
    يُعيد True إذا كان التنسيق صحيحاً، أو يُرسل رسالة خطأ تفصيلية ويُعيد False.
    """
    # نفس الصيغة التي تستخدمها format_news_item تماماً
    sep_line = f"\n{separator}" if separator else ""
    test_text = f"{label}\n\n📰 عنوان الخبر التجريبي\n🗞 مصدر تجريبي{sep_line}\n{signature}"
    try:
        bot.send_message(uid, test_text, parse_mode="Markdown")
        return True
    except Exception as e:
        err = str(e)
        if "can't parse" in err or "parse entities" in err or "400" in err:
            bot.send_message(
                uid,
                "❌ خطأ في التنسيق!\n\n"
                "النص الذي أدخلته يحتوي على رموز Markdown غير صحيحة أو غير مغلقة.\n\n"
                "الرموز التي قد تسبب مشكلة اذا ما كانت مغلقة:\n"
                "  * للعريض: يجب يكون *نص* (مفتوح ومغلق)\n"
                "  _ للمائل: يجب يكون _نص_ (مفتوح ومغلق)\n"
                "  ` للكود: يجب يكون `نص` (مفتوح ومغلق)\n\n"
                "للنص العادي: اكتب النص بدون أي رموز خاصة.\n\n"
                "لم يتم حفظ التغيير — حاول مجدداً."
            )
        return False

def _auto_translate_setting(arabic_text: str, setting_type: str = "label") -> dict:
    """
    يترجم نص عربي (label أو signature) لجميع اللغات الـ 13 باستخدام Gemini AI.
    يُرجع dict: {lang_key: translated_text}
    """
    _ALL_LANGS_FOR_TRANS = {
        "العربية 🇮🇶":   "Arabic",
        "English 🇬🇧":   "English",
        "Русский 🇷🇺":   "Russian",
        "فارسی 🇮🇷":     "Persian",
        "हिन्दी 🇮🇳":    "Hindi",
        "Português 🇧🇷": "Portuguese",
        "Türkçe 🇹🇷":   "Turkish",
        "اردو 🇵🇰":     "Urdu",
        "Deutsch 🇩🇪":  "German",
        "Українська 🇺🇦":"Ukrainian",
        "Italiano 🇮🇹": "Italian",
        "Español 🇲🇽":  "Spanish",
        "Français 🇫🇷": "French",
    }
    result = {"العربية 🇮🇶": arabic_text}  # النسخة الأصلية العربية دائماً محفوظة
    if not _AI_AVAILABLE or not _AI_MODEL:
        return result
    if setting_type == "label":
        context = "This is a short news label/tag (e.g. Breaking News, Latest). Keep it short, 1-5 words."
    else:
        context = "This is a news bot signature line. Keep emojis if any. Preserve @username handles as-is."
    lang_list = "\n".join(f"- {lang}: {eng}" for lang, eng in _ALL_LANGS_FOR_TRANS.items() if lang != "العربية 🇮🇶")
    prompt = (
        f"Translate the following Arabic text to each language listed below.\n"
        f"Context: {context}\n"
        f"Arabic text: {arabic_text}\n\n"
        f"Return ONLY a JSON object with language names as keys and translations as values.\n"
        f"Example format: {{\"English 🇬🇧\": \"...\", \"Русский 🇷🇺\": \"...\"}}\n\n"
        f"Languages to translate to:\n{lang_list}"
    )
    try:
        import json as _json
        holder = [None]
        def _gen():
            try:
                _r_ai = _ai_generate(prompt)
                if _r_ai:
                    holder[0] = _r_ai
            except Exception as _exc:
                _log_exc(_exc)
        _AI_EXECUTOR.submit(_gen).result(timeout=20)
        raw = holder[0]
        if not raw:
            return result
        # استخراج JSON من الرد
        raw = raw.strip()
        if raw.startswith("```"):
            raw = raw.split("```")[1]
            if raw.startswith("json"):
                raw = raw[4:]
        raw = raw.strip()
        parsed = _json.loads(raw)
        if isinstance(parsed, dict):
            for lang_key in _ALL_LANGS_FOR_TRANS:
                if lang_key in parsed:
                    result[lang_key] = str(parsed[lang_key]).strip()
    except Exception as _exc:
        _log_exc(_exc)
    return result


def _nf_label_step(message):
    if not is_admin(message.from_user.id):
        return
    new_label = message.text.strip()
    sep = news_settings.get("separator", "━━━━━━━━━━━━━━")
    sig = news_settings.get("signature", "عبر بوت أخبار العالم\n@Iraqnowbot")
    if not _validate_news_format(message.from_user.id, new_label, sep, sig):
        return
    news_settings["label"] = new_label
    # ترجمة تلقائية لجميع اللغات
    wait_m = bot.send_message(message.from_user.id, "🌐 *جاري الترجمة لجميع اللغات...*", parse_mode="Markdown")
    def _do_translate():
        translations = _auto_translate_setting(new_label, "label")
        news_settings["label_i18n"] = translations
        save_news_settings()
        langs_count = len(translations)
        try:
            bot.edit_message_text(
                f"✅ تم حفظ العنوان الجديد وترجمته لـ *{langs_count} لغة* تلقائياً.\n\n"
                f"🇮🇶 عربي: `{new_label}`\n"
                f"🇬🇧 إنجليزي: `{translations.get('English 🇬🇧', '—')}`\n"
                f"🇩🇪 ألماني: `{translations.get('Deutsch 🇩🇪', '—')}`\n"
                f"🇫🇷 فرنسي: `{translations.get('Français 🇫🇷', '—')}`",
                message.from_user.id, wait_m.message_id, parse_mode="Markdown")
        except Exception:
            bot.send_message(message.from_user.id, "✅ تم حفظ العنوان وترجمته لجميع اللغات.")
    _AI_EXECUTOR.submit(_do_translate)

def _nf_sep_step(message):
    if not is_admin(message.from_user.id):
        return
    val = message.text.strip()
    new_sep = "" if val == "بدون" else val
    label = news_settings.get("label", "🚨 خبر عاجل")
    sig = news_settings.get("signature", "عبر بوت أخبار العالم\n@Iraqnowbot")
    if not _validate_news_format(message.from_user.id, label, new_sep, sig):
        return
    news_settings["separator"] = new_sep
    save_news_settings()
    bot.send_message(message.from_user.id, "✅ تم حفظ الفاصل الجديد بنجاح.")

def _nf_sig_step(message):
    if not is_admin(message.from_user.id):
        return
    new_sig = message.text.strip().replace("\\n", "\n")
    label = news_settings.get("label", "🚨 خبر عاجل")
    sep = news_settings.get("separator", "━━━━━━━━━━━━━━")
    if not _validate_news_format(message.from_user.id, label, sep, new_sig):
        return
    news_settings["signature"] = new_sig
    wait_m = bot.send_message(message.from_user.id, "🌐 *جاري ترجمة التوقيع لجميع اللغات...*", parse_mode="Markdown")
    def _do_translate():
        translations = _auto_translate_setting(new_sig, "signature")
        news_settings["signature_i18n"] = translations
        save_news_settings()
        try:
            bot.edit_message_text(
                f"✅ تم حفظ التوقيع وترجمته لـ *{len(translations)} لغة* تلقائياً.\n\n"
                f"🇮🇶 عربي: `{new_sig[:60]}`\n"
                f"🇬🇧 إنجليزي: `{translations.get('English 🇬🇧', '—')[:60]}`",
                message.from_user.id, wait_m.message_id, parse_mode="Markdown")
        except Exception:
            bot.send_message(message.from_user.id, "✅ تم حفظ التوقيع وترجمته لجميع اللغات.")
    _AI_EXECUTOR.submit(_do_translate)

def get_news_signature(lang=None):
    sep = news_settings.get("separator", "━━━━━━━━━━━━━━")
    custom_sig = news_settings.get("signature", _DEFAULT_NEWS_SIGNATURE)
    _is_custom = (custom_sig != _DEFAULT_NEWS_SIGNATURE)
    if _is_custom:
        # يستخدم النسخة المترجمة لهذه اللغة إن وُجدت
        i18n = news_settings.get("signature_i18n", {})
        sig = i18n.get(lang, custom_sig) if lang else custom_sig
    elif lang and lang in _NEWS_SIGNATURE_LOCALIZED:
        sig = _NEWS_SIGNATURE_LOCALIZED[lang]
    else:
        sig = _DEFAULT_NEWS_SIGNATURE
    return f"\n{sep}\n{sig}"

